
public class Person {
    public Person(){}
    public Person(int id, String name, String surname, String birthDate, String race, String number, String email) {
        Id = id;
        Name = name;
        Surname = surname;
        BirthDate = birthDate;
        Race = race;
        Number = number;
        Email = email;
    }

    public int Id;
    public String Name;
    public String Surname;
    public String BirthDate;
    public String Race;
    public String Number;
    public String Email;
}
